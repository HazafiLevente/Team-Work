CREATE DATABASE IF NOT EXISTS datas;
USE datas;

CREATE TABLE IF NOT EXISTS Guitar (
    GuitarID int,
    Name varchar(255),
    Modell varchar(255),
    Type varchar(255),
    Color varchar(255),
    Body varchar(255),
    Neck varchar(255),
    Fretboard varchar(255),
    Headstock varchar(255),
    TuningPegs varchar(255),
    Nut varchar(255),
    Bridge varchar(255),
    Pickups varchar(255),
    Pickguard varchar(255),
    Strings varchar(255),
    Controls varchar(255),
    StrapButtons varchar(255)
);

        
